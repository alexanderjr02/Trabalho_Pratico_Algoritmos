# Microatividade 4: Estrutura de Repetição for

# Definindo a variável texto
texto = "Olá, laço for."

# Estrutura de repetição for para iterar sobre os caracteres do texto
for item in texto:
    print("Caractere:", item)

# Estrutura de repetição for para iterar sobre um intervalo numérico
for numero in range(1, 11):
    print("Número do intervalo:", str(numero))
